@Composable
fun What_to_eat_androidTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Dynamic color is available on Android 12+
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            val insetsController = WindowCompat.getInsetsController(window, view)

            // Use the modern approach for system bar colors
            WindowCompat.setDecorFitsSystemWindows(window, false)

            // Set status bar appearance
            insetsController.isAppearanceLightStatusBars = !darkTheme
            insetsController.isAppearanceLightNavigationBars = !darkTheme

            // For API 23+ (Android 6.0+), we can still use the window colors as fallback
            // but this is handled by the WindowInsetsController now
            if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.M) {
                // The system bars will automatically adapt to the theme
                // No need to manually set colors as they're handled by the MaterialTheme
            }
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}